#{icon('large_orange_diamond')} *TokenBar*
Пример кода `380971234567:01234567`
Сайт платежной системы tokenbar.net

Платежи TokenBar зачисляются в автоматическом режиме. Комиссий мы не берем. Данные коды пополнения можно купить за наличные деньги в любом терминале EasyPay.

#{icon('large_orange_diamond')} *Easypay*
Пример кода `21:4699971`
Пример чека и как найти код пополнения /easypaysample

EasyPay платежи принимаются в автоматическом режиме. Без комиссий. Код пополнения - это время оплаты с Вашего чека *плюс 1 минута* и без пробела номер терминала. Таким образом, если на чеке время `21:05` и номер терминала `99999`, то код пополнения будет `21:0699999`.

Только платежи сделанные в терминале обрабатываются автоматически. Переводы с карты или с другого кошелька зачисляются на баланс по запросу в службу поддержки.

